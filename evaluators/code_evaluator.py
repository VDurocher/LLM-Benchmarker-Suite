"""
Generated code quality evaluator.

Static analysis of Python code produced by the model:
1. Syntax validity (AST parsing)
2. Presence of required constructs (functions, classes, imports)
3. Detection of dangerous patterns (eval, exec, os.system)
4. Light PEP8 compliance (indentation, line length)
5. Edge case coverage via assertion verification

This module is particularly useful for LLMs used in code generation
contexts (coding assistants, DevOps automation).
"""

from __future__ import annotations

import ast
import re
from typing import Any

from evaluators.base_evaluator import BaseEvaluator, EvaluationResult

# Dangerous code patterns — always flagged as critical violations
_DANGEROUS_PATTERNS: list[tuple[str, str]] = [
    (r"\beval\s*\(", "eval() detected — injection risk"),
    (r"\bexec\s*\(", "exec() detected — arbitrary execution risk"),
    (r"\bos\.system\s*\(", "os.system() detected — shell injection risk"),
    (r"\bsubprocess\.call\s*\(", "subprocess.call() without validation"),
    (r"__import__\s*\(", "dynamic __import__() detected"),
    (r"open\s*\([^)]*['\"]w['\"]\", \"File write without error handling"),
]

# Maximum line length (PEP8)
_MAX_LINE_LENGTH: int = 120


def _parse_ast_safely(code: str) -> tuple[ast.Module | None, str | None]:
    """
    Attempts to parse the code into an AST. Returns (module, None) or (None, error).
    """
    try:
        module = ast.parse(code)
        return module, None
    except SyntaxError as exc:
        return None, f"SyntaxError line {exc.lineno}: {exc.msg}"


class CodeEvaluator(BaseEvaluator):
    """
    Evaluates the quality and security of Python code generated by the model.

    Configuration parameters via `metadata`:
    - `required_functions` (list[str]): expected function names.
    - `required_classes` (list[str]): expected class names.
    - `required_imports` (list[str]): modules that must be imported.
    - `check_dangerous_patterns` (bool): enables dangerous pattern detection (default: True).
    - `check_pep8_lines` (bool): checks line length (default: True).
    - `expected_return_type` (str): expected return type for the main function.
    """

    def __init__(self, threshold: float = 0.75) -> None:
        super().__init__(name="code_quality", threshold=threshold)

    def _run_evaluation(
        self,
        prompt: str,
        expected_output: str,
        model_output: str,
        metadata: dict[str, Any],
    ) -> EvaluationResult:
        violations: list[str] = []
        checks_passed: list[str] = []

        # Extract code block if wrapped in Markdown backticks
        code = _extract_code_block(model_output)

        # --- AST syntax validation ---
        parsed_module, syntax_error = _parse_ast_safely(code)
        if syntax_error:
            violations.append(syntax_error)
            # If code does not parse, further checks are meaningless
            return EvaluationResult(
                evaluator_name=self._name,
                passed=False,
                score=0.0,
                details={
                    "violations": violations,
                    "checks_passed": checks_passed,
                    "ast_parsed": False,
                    "code_length_lines": code.count("\n") + 1,
                },
            )

        checks_passed.append("syntax_valid_ast")

        # Collect AST nodes for subsequent checks
        function_names: set[str] = set()
        class_names: set[str] = set()
        import_names: set[str] = set()

        for node in ast.walk(parsed_module):
            if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)):
                function_names.add(node.name)
            elif isinstance(node, ast.ClassDef):
                class_names.add(node.name)
            elif isinstance(node, ast.Import):
                for alias in node.names:
                    import_names.add(alias.name.split(".")[0])
            elif isinstance(node, ast.ImportFrom):
                if node.module:
                    import_names.add(node.module.split(".")[0])

        # --- Required functions check ---
        for func_name in metadata.get("required_functions", []):
            if func_name in function_names:
                checks_passed.append(f"function_present:{func_name}")
            else:
                violations.append(f"Required function missing: '{func_name}'")

        # --- Required classes check ---
        for class_name in metadata.get("required_classes", []):
            if class_name in class_names:
                checks_passed.append(f"class_present:{class_name}")
            else:
                violations.append(f"Required class missing: '{class_name}'")

        # --- Required imports check ---
        for import_name in metadata.get("required_imports", []):
            if import_name in import_names:
                checks_passed.append(f"import_present:{import_name}")
            else:
                violations.append(f"Required import missing: '{import_name}'")

        # --- Dangerous pattern detection ---
        if metadata.get("check_dangerous_patterns", True):
            for pattern, description in _DANGEROUS_PATTERNS:
                if re.search(pattern, code):
                    violations.append(f"[SECURITY] {description}")
                else:
                    checks_passed.append(f"safe_pattern:{pattern[:20]}")

        # --- PEP8 line length check ---
        if metadata.get("check_pep8_lines", True):
            long_lines = [
                i + 1
                for i, line in enumerate(code.splitlines())
                if len(line) > _MAX_LINE_LENGTH
            ]
            if long_lines:
                violations.append(
                    f"Lines too long (>{_MAX_LINE_LENGTH} chars): {long_lines[:5]}"
                )
            else:
                checks_passed.append("pep8_line_length_ok")

        # --- Composite score ---
        total_checks = len(checks_passed) + len(violations)
        score = len(checks_passed) / total_checks if total_checks > 0 else 1.0
        passed = score >= self._threshold and not any(
            "[SECURITY]" in v for v in violations
        )

        return EvaluationResult(
            evaluator_name=self._name,
            passed=passed,
            score=round(score, 4),
            details={
                "threshold": self._threshold,
                "violations": violations,
                "checks_passed": checks_passed,
                "ast_parsed": True,
                "function_names_found": sorted(function_names),
                "class_names_found": sorted(class_names),
                "import_names_found": sorted(import_names),
                "code_length_lines": code.count("\n") + 1,
                "has_security_violations": any("[SECURITY]" in v for v in violations),
            },
        )


def _extract_code_block(text: str) -> str:
    """
    Extracts code from a Markdown ```python ... ``` block if it exists,
    otherwise returns the text as-is.
    """
    match = re.search(r"```(?:python)?\n(.*?)```", text, re.DOTALL)
    if match:
        return match.group(1).strip()
    return text.strip()
